# Dictionary Methods with Syntax

# dict.get(key, default)          # Returns value of key; if not found, returns default  
# dict.keys()                     # Returns all keys as a view object  
# dict.values()                   # Returns all values as a view object  
# dict.items()                    # Returns key-value pairs as a view object  
# dict.pop(key, default)          # Removes key and returns its value; if key not found, returns default  
# dict.popitem()                  # Removes and returns the last key-value pair  
# dict.update(other_dict)         # Updates dictionary with another dictionary or iterable  
# dict.clear()                    # Removes all items from dictionary  
# dict.setdefault(key, default)   # Returns value of key; if not found, inserts key with default value  
# dict.copy()                     # Returns a shallow copy of the dictionary  
